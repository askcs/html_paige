AdminSession.prototype.isLogin = function() {
	return (this.sessionKey != null && this.sessionKey != "");
}

AdminSession.prototype.setSessionKey = function(sKey) {
	this.sessionKey = sKey;
	localStorage.setItem("adminSessionKey", sKey);
}

AdminSession.prototype.authenticator = function(){
	if(!this.isLogin()){
		window.location = "admin_login.html";
	}
}

AdminSession.prototype.logoff = function(){
	// Do serverside logout:
	$.ajax(this.appServices + "logout/", {
		headers : {
			'X-SESSION_ID' : this.sessionKey
		},
		xhrFields : {
			withCredentials : true
		}
	});
	
	this.setSessionKey("");	
	window.location = "admin_login.html";
}

